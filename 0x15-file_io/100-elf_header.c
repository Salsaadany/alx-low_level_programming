/**
 * check_elf - prpse is checkin whthr it's an ELF txtf.
 * @e_ident: A pointer to the arr housing the ELF mgc digits.
 *
 * Description: If its not ELFf - exit code 98.
 */
void check_elf(unsigned char *e_ident)
{
	int j;

	for (j = 0; j < 4; j++)
	{
		if (e_ident[j] != 127 &&
			e_ident[j] != 'E' &&
			e_ident[j] != 'L' &&
			e_ident[j] != 'F')
		{
			dprintf(STDERR_FILENO, "Error: Not an ELF file\n");
			exit(98);
		}
	}
}

/**
 * print_magic - prpse is displaying header's mgc digits
 * @e_ident: A pointer to arr housing ELF mgc digits
 *
 * Description: Mgc digits detached by spaces
 */
void print_magic(unsigned char *e_ident)
{
	int j;

	printf("  Magic:   ");

	for (j = 0; j < EI_NIDENT; j++)
	{
		printf("%02x", e_ident[j]);

		if (j == EI_NIDENT - 1)
			printf("\n");
		else
			printf(" ");
	}
}

/**
 * print_class - displaying header's class
 * @e_ident: A pointer to arr housing the class
 */
void print_class(unsigned char *e_ident)
{
	printf("  Class:                             ");

	switch (e_ident[EI_CLASS])
	{
	case ELFCLASSNONE:
		printf("none\n");
		break;
	case ELFCLASS32:
		printf("ELF32\n");
		break;
	case ELFCLASS64:
		printf("ELF64\n");
		break;
	default:
		printf("<unknown: %x>\n", e_ident[EI_CLASS]);
	}
}

